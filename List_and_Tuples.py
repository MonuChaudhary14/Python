# A list is a collection of ordered, mutable (changeable) items. Lists can contain any data type, including other lists.

# List is like an array

my_list = [10, 20, 30, 40]
mixed_list = [1, "hello", 3.14, [2, 4]]


print(my_list[3])
print(mixed_list[3])
print(mixed_list[3][0])  # This will give 2 because it will access the 0 index of 4th element

# Splicing of list

print(my_list[1:3])


